-- 2. Server-side rate limiting functions
-- ============================================================================

-- Create a table to track rate limit violations (for server-side enforcement)
CREATE TABLE IF NOT EXISTS public.rate_limit_violations (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id uuid REFERENCES auth.users(id),
  action_name text NOT NULL,
  violation_count int DEFAULT 1,
  first_violation_at timestamptz DEFAULT now(),
  last_violation_at timestamptz DEFAULT now(),
  created_at timestamptz DEFAULT now(),
  UNIQUE (user_id, action_name)
);

-- Enable RLS on rate_limit_violations
ALTER TABLE public.rate_limit_violations ENABLE ROW LEVEL SECURITY;

-- Users can only see their own violations
DROP POLICY IF EXISTS "Users can view own rate limit violations" ON public.rate_limit_violations;
CREATE POLICY "Users can view own rate limit violations"
  ON public.rate_limit_violations FOR SELECT
  TO authenticated
  USING (user_id = auth.uid());

-- Users can only insert their own violations
DROP POLICY IF EXISTS "Users can insert own rate limit violations" ON public.rate_limit_violations;
CREATE POLICY "Users can insert own rate limit violations"
  ON public.rate_limit_violations FOR INSERT
  TO authenticated
  WITH CHECK (user_id = auth.uid());

-- Function to check and record rate limit violations
CREATE OR REPLACE FUNCTION public.check_rate_limit(
  p_action_name text,
  p_max_attempts int DEFAULT 10,
  p_window_minutes int DEFAULT 60
)
RETURNS boolean
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
  DECLARE
    current_user_id uuid := auth.uid();
    violation_count int;
    is_allowed boolean;
  BEGIN
    IF current_user_id IS NULL THEN
      -- No authenticated user — allow but log (could implement IP-based limiting)
      RETURN true;
    END IF;
    
    -- Count violations in the time window
    SELECT COALESCE(sum(violation_count), 0)
    INTO violation_count
    FROM public.rate_limit_violations
    WHERE user_id = current_user_id
      AND action_name = p_action_name
      AND last_violation_at > now() - (p_window_minutes || ' minutes')::interval;
    
    IF violation_count >= p_max_attempts THEN
      RETURN false;
    END IF;
    
    RETURN true;
  END;
$$;

-- Function to record a rate limit violation (call after rejected action)
CREATE OR REPLACE FUNCTION public.record_rate_limit_violation(p_action_name text)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
  DECLARE
    current_user_id uuid := auth.uid();
  BEGIN
    IF current_user_id IS NULL THEN
      RETURN;
    END IF;
    
    -- Insert or update violation record
    INSERT INTO public.rate_limit_violations (user_id, action_name)
    VALUES (current_user_id, p_action_name)
    ON CONFLICT (user_id, action_name) DO UPDATE
      SET violation_count = rate_limit_violations.violation_count + 1,
          last_violation_at = now();
  END;
$$;

