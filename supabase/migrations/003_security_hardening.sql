-- 1. Hardened get_user_stats — add ownership verification
-- ============================================================================

-- Drop existing function to replace with hardened version
DROP FUNCTION IF EXISTS public.get_user_stats(uuid);

-- New version with explicit ownership check via auth.uid()
CREATE OR REPLACE FUNCTION public.get_user_stats(p_uid uuid)
RETURNS json
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
AS $$
  DECLARE
    is_owner BOOLEAN;
    is_moderator BOOLEAN;
    result json;
  BEGIN
    -- Check if the authenticated user owns this profile
    SELECT (auth.uid() = p_uid) INTO is_owner;
    
    -- Check if the authenticated user is a moderator
    SELECT EXISTS (
      SELECT 1 FROM public.users
      WHERE users.id = auth.uid() AND users.role = 'moderator'
    ) INTO is_moderator;
    
    -- Only allow if owner or moderator
    IF NOT is_owner AND NOT is_moderator THEN
      RAISE EXCEPTION 'Access denied: You can only view your own stats';
    END IF;
    
    -- Return stats
    SELECT json_build_object(
      'question_count', (SELECT count(*) FROM public.questions WHERE author_uid = p_uid),
      'answer_count',   (SELECT count(*) FROM public.answers   WHERE author_uid = p_uid),
      'bookmark_count', (SELECT count(*) FROM public.bookmarks WHERE user_id   = p_uid)
    ) INTO result;
    
    RETURN result;
  END;
$$;

